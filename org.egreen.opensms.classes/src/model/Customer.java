package model;

/**
 *
 * @author ChanakaDe
 */
public class Customer {
    private int userId;
    private String name;
    private String remark;

    public Customer() {
    }

    public Customer(int userId, String name, String remark) {
        this.userId = userId;
        this.name = name;
        this.remark = remark;
    }

    /**
     * @return the userId
     */
    public int getUserId() {
        return userId;
    }

    /**
     * @param userId the userId to set
     */
    public void setUserId(int userId) {
        this.userId = userId;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the remark
     */
    public String getRemark() {
        return remark;
    }

    /**
     * @param remark the remark to set
     */
    public void setRemark(String remark) {
        this.remark = remark;
    }
    
    
}
